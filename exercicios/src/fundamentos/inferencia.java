package fundamentos;

public class inferencia {

    public static void main(String[] args) {

        double a = 4.5;
        System.out.println(a);

        a = 8;
        System.out.println(a);

        var b = 5.5;
        System.out.println(b);

        var c = "texto";
        System.out.println(c);

        var d = 190;
        System.out.println(d);

        var e = "TEXTO";
        System.out.println(e);

    }
}
